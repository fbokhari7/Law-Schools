CREATE DATABASE Law_School;

CREATE TABLE law_schools (
    school_id INT PRIMARY KEY, 
    school_name VARCHAR(255) NOT NULL,
    city VARCHAR(255),
    state VARCHAR(255),
    accreditation BOOLEAN NOT NULL
);

CREATE TABLE admissions (
    admissions_id CHAR(6) PRIMARY KEY, 
    school_id INT NOT NULL,
    total_applications INT NOT NULL,
    total_offers INT NOT NULL,
    total_fy_class INT NOT NULL,
    ugpa_75th_percentile DECIMAL(3, 2),
    ugpa_50th_percentile DECIMAL(3, 2),
    ugpa_25th_percentile DECIMAL(3, 2),
    lsat_75th_percentile INT,
    lsat_50th_percentile INT,
    lsat_25th_percentile INT,
    FOREIGN KEY (school_id) REFERENCES law_schools(school_id) ON DELETE CASCADE
);

CREATE TABLE bar_exam (
    bar_exam_id CHAR(6) PRIMARY KEY, 
    school_id INT NOT NULL,
    graduates_in_2023 INT NOT NULL,
    total_first_time_takers INT NOT NULL,
    grads_no_bar_2023 INT NOT NULL, 
    grads_no_bar_percentage DECIMAL(5, 2), 
    grads_no_info INT NOT NULL, 
    grads_no_info_percentage DECIMAL(5, 2), 
    total_first_time_passers INT NOT NULL,
    avg_school_pass_percent DECIMAL(5, 2),
    FOREIGN KEY (school_id) REFERENCES law_schools(school_id) ON DELETE CASCADE
);

CREATE TABLE employment_data (
    employment_id CHAR(6) PRIMARY KEY, 
    school_id INT NOT NULL, 
    full_time_employed INT NOT NULL, 
    under_employed INT NOT NULL, 
    unknown INT NOT NULL, 
    biglaw_100 INT NOT NULL, 
    public_service INT NOT NULL, 
    fed_clerks INT NOT NULL, 
    law_school_funded INT NOT NULL,
    FOREIGN KEY (school_id) REFERENCES law_schools(school_id) ON DELETE CASCADE
);

CREATE TABLE grants (
    grants_id CHAR(6) PRIMARY KEY, 
    school_id INT NOT NULL, 
    tuition DECIMAL(10, 2) NOT NULL, 
    percent_students_receiving_grants DECIMAL(5, 2) NOT NULL,
    FOREIGN KEY (school_id) REFERENCES law_schools(school_id) ON DELETE CASCADE
);

CREATE TABLE debt (
    debt_id CHAR(6) PRIMARY KEY, 
    school_id INT NOT NULL, 
    institution_type VARCHAR(100) NOT NULL, 
    avg_loan_disbursement DECIMAL(12, 2) NOT NULL, 
    percent_borrowing DECIMAL(5, 2) NOT NULL,
    FOREIGN KEY (school_id) REFERENCES law_schools(school_id) ON DELETE CASCADE
);

CREATE TABLE gender (
    gender_id CHAR(6) PRIMARY KEY, 
    school_id INT NOT NULL, 
    total_fy_class_total INT NOT NULL, 
    total_fy_class_men INT NOT NULL, 
    total_fy_class_women INT NOT NULL, 
    total_fy_class_another_gender_identity INT DEFAULT 0, 
    total_fy_class_prefer_not_to_report INT DEFAULT 0,
    FOREIGN KEY (school_id) REFERENCES law_schools(school_id) ON DELETE CASCADE
);

CREATE TABLE race_demographics (
    race_demographics_id CHAR(6) PRIMARY KEY, 
    school_id INT NOT NULL, 
    race_unknown INT DEFAULT 0, 
    white INT DEFAULT 0, 
    hispanic INT DEFAULT 0, 
    american_indian INT DEFAULT 0, 
    asian INT DEFAULT 0, 
    african_american INT DEFAULT 0, 
    native_hawaiian_pacific_islander INT DEFAULT 0, 
    two_or_more_races INT DEFAULT 0,
    FOREIGN KEY (school_id) REFERENCES law_schools(school_id) ON DELETE CASCADE
);

