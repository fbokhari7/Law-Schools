FROM law_schools ls
JOIN admissions a ON ls.school_id = a.school_id
ORDER BY a.total_offers DESC
LIMIT 5;

/* 
School with the highest debt-to-grant ratio.
*/
SELECT ls.school_name, 
       d.avg_loan_disbursement / g.tuition AS "Debt-to-Grant Ratio"
FROM law_schools ls
JOIN debt d ON ls.school_id = d.school_id
JOIN grants g ON ls.school_id = g.school_id
WHERE g.tuition > 0
ORDER BY "Debt-to-Grant Ratio" DESC
LIMIT 1;

