       CASE 
           WHEN b.avg_school_pass_percent >= 90 THEN 'Excellent'
           WHEN b.avg_school_pass_percent >= 75 THEN 'Good'
           ELSE 'Needs Improvement'
       END AS "Performance Category"
FROM law_schools ls
JOIN bar_exam b ON ls.school_id = b.school_id;


/* 
Employment data by school and overall totals.
*/
SELECT ls.school_name, 
       SUM(e.full_time_employed) AS "Total Employed", 
       SUM(e.under_employed) AS "Under Employed"
FROM law_schools ls
JOIN employment_data e ON ls.school_id = e.school_id
GROUP BY ROLLUP(ls.school_name);

/* 
Schools with the highest percentage of students receiving grants and their employment outcomes.
*/
SELECT ls.school_name, g.percent_students_receiving_grants, e.full_time_employed, e.public_service
FROM law_schools ls
JOIN grants g ON ls.school_id = g.school_id
JOIN employment_data e ON ls.school_id = e.school_id
ORDER BY g.percent_students_receiving_grants DESC
LIMIT 5;

/* 
Schools with the highest percentage of African American students.
*/
SELECT ls.school_name, 
       rd.african_american / rd.race_unknown AS "Percentage African American"
FROM law_schools ls
JOIN race_demographics rd ON ls.school_id = rd.school_id
WHERE rd.race_unknown > 0
ORDER BY "Percentage African American" DESC
LIMIT 10;


