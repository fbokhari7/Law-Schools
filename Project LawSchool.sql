/*
Schools with tuition costs above the average tuition, 
along with their grant percentages
*/
SELECT ls.school_name, g.tuition, g.percent_students_receiving_grants
FROM law_schools ls
JOIN grants g ON ls.school_id = g.school_id
WHERE g.tuition > (SELECT AVG(tuition) FROM grants)
ORDER BY g.tuition DESC;

/*
Schools with the highest underemployment rate among graduates
*/
SELECT ls.school_name, ed.under_employed
FROM law_schools ls
JOIN employment_data ed ON ls.school_id = ed.school_id
ORDER BY ed.under_employed DESC
LIMIT 10;

/*
Average debt and borrowing percentage for public 
and private schools
*/
SELECT d.institution_type, 
       AVG(d.avg_loan_disbursement) AS avg_debt, 
       AVG(d.percent_borrowing) AS avg_borrowing
FROM debt d
GROUP BY d.institution_type;

/*
Top Five schools with the most racially diverse student populations
Find the schools with the highest full-time employment rate post-graduation
*/
SELECT ls.school_name, 
       rd.white, rd.hispanic, rd.african_american, rd.asian, 
       rd.native_hawaiian_pacific_islander, rd.two_or_more_races
FROM law_schools ls
JOIN race_demographics rd ON ls.school_id = rd.school_id
ORDER BY (rd.white + rd.hispanic + rd.african_american + rd.asian 
          + rd.native_hawaiian_pacific_islander + rd.two_or_more_races) DESC
LIMIT 5;

/*
Top Ten schools with the highest full-time employment
after post-graduation
*/
SELECT ls.school_name, ed.full_time_employed
FROM law_schools ls
JOIN employment_data ed ON ls.school_id = ed.school_id
ORDER BY ed.full_time_employed DESC
LIMIT 10;

/*
Top Ten schools with the highest bar exam pass rates
*/
SELECT ls.school_name, be.avg_school_pass_percent
FROM law_schools ls
JOIN bar_exam be ON ls.school_id = be.school_id
ORDER BY be.avg_school_pass_percent DESC
LIMIT 10;


